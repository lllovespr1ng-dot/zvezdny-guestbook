const params = new URLSearchParams(window.location.search);
const roomFromUrl = params.get("room");
const roomStorageKey = "zvezdnyGuestRoom";
const roomNumber = document.querySelector("#roomNumber");
const roomGate = document.querySelector("#roomGate");
const roomGateForm = document.querySelector("#roomGateForm");
const roomGateInput = document.querySelector("#roomGateInput");
const roomInputs = [
  document.querySelector("#requestRoom"),
  document.querySelector("#feedbackRoom")
];

let currentLang = "ru";
const requestModal = document.querySelector("#requestModal");
const feedbackModal = document.querySelector("#feedbackModal");
const requestTitle = document.querySelector("#requestTitle");
const toast = document.querySelector("#toast");

function normalizeRoom(value) {
  return value.trim().replace(/[^\dA-Za-zА-Яа-я-]/g, "");
}

function setRoom(room, options = {}) {
  const normalizedRoom = normalizeRoom(room);
  if (!normalizedRoom) {
    if (options.showError) {
      showToast(translations[currentLang].roomRequired);
    }
    return false;
  }

  roomNumber.textContent = normalizedRoom;
  roomInputs.forEach((input) => {
    input.value = normalizedRoom;
  });
  localStorage.setItem(roomStorageKey, normalizedRoom);
  roomGate.classList.remove("visible");
  document.body.classList.remove("room-gate-open");

  if (options.showToast) {
    showToast(translations[currentLang].roomSaved);
  }
  return true;
}

function showRoomGate() {
  roomGate.classList.add("visible");
  document.body.classList.add("room-gate-open");
  window.setTimeout(() => roomGateInput.focus(), 50);
}

function setLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (translations[lang][key]) {
      node.textContent = translations[lang][key];
    }
  });
  document.querySelectorAll(".lang-switch").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
  });
}

function showToast(message) {
  toast.textContent = message;
  toast.classList.add("visible");
  window.clearTimeout(showToast.timer);
  showToast.timer = window.setTimeout(() => {
    toast.classList.remove("visible");
  }, 2800);
}

document.querySelectorAll(".lang-switch").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

roomGateForm.addEventListener("submit", (event) => {
  event.preventDefault();
  setRoom(roomGateInput.value, { showError: true, showToast: true });
});

document.querySelector("#editRoom").addEventListener("click", () => {
  roomGateInput.value = roomNumber.textContent === "304" ? "" : roomNumber.textContent;
  showRoomGate();
});

document.querySelectorAll("[data-request]").forEach((button) => {
  button.addEventListener("click", () => {
    const serviceName = button.closest(".service-item")?.querySelector("h3")?.textContent
      || button.textContent.trim();
    requestTitle.textContent = serviceName;
    requestModal.showModal();
  });
});

document.querySelector("#openFeedback").addEventListener("click", () => {
  feedbackModal.showModal();
});

document.querySelectorAll(".modal form").forEach((form) => {
  form.addEventListener("submit", (event) => {
    event.preventDefault();
    form.closest("dialog").close();
    showToast(translations[currentLang].requestPrepared);
  });
});

document.querySelectorAll(".modal-close").forEach((button) => {
  button.addEventListener("click", () => {
    button.closest("dialog").close();
  });
});

const savedRoom = roomFromUrl || localStorage.getItem(roomStorageKey);
if (savedRoom) {
  setRoom(savedRoom);
} else {
  roomNumber.textContent = "не указан";
  roomInputs.forEach((input) => {
    input.value = "";
  });
  showRoomGate();
}
